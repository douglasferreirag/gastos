    private void jmRemoverMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jmRemoverMouseClicked
        // Trata da remoção e atualização de códigos.


            jmRemover.setVisible(false);

            String[][] parametro = new String[1][2];

            parametro[0][0] = "Double";

            parametro[0][1] = jtxtCodigo.getText();

            new Funcoes().executa("delete from catalogo where codigo = ?",
                parametro, "Remoção na tabela de catalogo realizada com sucesso.");

            if( new Funcoes().retornar("select quantidade from item where codigoCatalogo = ?", "quantidade", parametro) != null)

                new Funcoes().executa("delete from item where codigoCatalogo = ?",
                    parametro, "Remoção na tabela de ítens realizada com sucesso. ");

            if( (new Funcoes().retornar("SELECT Max(codigo) "
            + "FROM catalogo ","Max(codigo)",null) != null) &&(Double.parseDouble(jtxtCodigo.getText()) != (Double)new Funcoes().retornar("SELECT Max(codigo) "
                + "FROM catalogo ","Max(codigo)",null)) )

                new Funcoes().executa( "update catalogo set codigo = codigo - 1 where codigo > ? ", parametro,"Atualização de códigos na tabela de catalogo realizada com sucesso.");

        
        
        
    }//GEN-LAST:event_jmRemoverMouseClicked

