    private void jtxtNomeMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtxtNomeMouseClicked
        //Vai para a tela de entrada.
       
        
        if(jtxtNome.getText().isEmpty() == true){
            
            EntradaValores.main(argumento, jtxtNome, false, jtxtUsuario.getText(), "Insira o lugar onde você achou o produto.");
            
        }
        
    }//GEN-LAST:event_jtxtNomeMouseClicked

    private void jmLimparMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jmLimparMouseClicked
        // Limpa o formulário.
        
        jtxtNome.setText("");
        
        new Funcoes().setaCodigo(jtxtCod,"SELECT Max(codigo) "
                          + "FROM pessoa ","Max(codigo)");
        
        trava = false;
        
        jmCadastro.setVisible(false);
        
    }//GEN-LAST:event_jmLimparMouseClicked

    private void jmCadastroMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jmCadastroMouseClicked
        // Trata do cadastro.
        
        jmCadastro.setVisible(false);
        
        String parametro[][] = new String[2][2];
        
        parametro[0][0] = "Double";
        
        parametro[0][1] = jtxtCod.getText();
        
        parametro[1][0] = "String";
        
        parametro[1][1] = jtxtNome.getText();
        
        new Funcoes().executa("INSERT INTO pessoa(codigo,nomecompleto) VALUES(?,?)", parametro, "Cadastro na tabela de pessoas realizado com sucesso.");
        
        
                
        
        
    }//GEN-LAST:event_jmCadastroMouseClicked

    private void jmMenuMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jmMenuMouseClicked
        //Vai para o menu principal e fecha o frame atual.
        
        MenuPrincipal.main(argumento,jtxtUsuario.getText());

        CadastroPessoa.this.dispose(); 
        
    }//GEN-LAST:event_jmMenuMouseClicked

    private void formWindowActivated(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowActivated
        // Verifico se posos liberar o botão de cadastro.
        
        if((jtxtNome.getText().isEmpty() == false) && (jmCadastro.isVisible() == false) && (trava == false) ){
            
                jmCadastro.setVisible(true);
                
                trava = true;
            
        }
        
    }//GEN-LAST:event_formWindowActivated

