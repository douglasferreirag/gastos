    private void jtCatalogoMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtCatalogoMousePressed
           
        // Retorna um valor.
        
        dtm =  (javax.swing.table.DefaultTableModel)jtCatalogo.getModel();
        
      
        
        switch(temp){
                
                case "Altera√ß√£o de dados":{
                    
                    if(jtCatalogo.getSelectedColumn() !=  3) {    
                    
                            JOptionPane.showMessageDialog(null,"Valor na coluna selecionada: " + dtm.getValueAt(jtCatalogo.getSelectedRow() ,jtCatalogo.getSelectedColumn()).toString() + "." , "System Message", JOptionPane.INFORMATION_MESSAGE);
                
                            jtxtReturn.setText(dtm.getValueAt(jtCatalogo.getSelectedRow() ,jtCatalogo.getSelectedColumn()).toString());
                
                            String nomeColuna = dtm.getColumnName(jtCatalogo.getSelectedColumn());
                            
                            JOptionPane.showMessageDialog(null,"Nome da coluna selecionada: " + nomeColuna + "." , "System Message", JOptionPane.INFORMATION_MESSAGE);
                
                            jtxtColumn.setText(nomeColuna);
                    
                            break;
                            
                    }
                    
                }
                
                case "Cadastro de √çtem": {
                    
                    if(jtCatalogo.getSelectedColumn() ==  0) {
                    
                            JOptionPane.showMessageDialog(null,"Valor na coluna selecionada: " + dtm.getValueAt(jtCatalogo.getSelectedRow() ,0).toString() + "." , "System Message", JOptionPane.INFORMATION_MESSAGE);
                
                            jtxtReturn.setText(dtm.getValueAt(jtCatalogo.getSelectedRow() ,0).toString());
                            
                            jtxtValor.setText(dtm.getValueAt(jtCatalogo.getSelectedRow() ,2).toString());
                            
                            
                    
                    }
                
                    else
                    
                        JOptionPane.showMessageDialog(null," N√£o selecionou a coluna do c√≥digo.", "System Message", JOptionPane.INFORMATION_MESSAGE);
                
                    break;
                    
                }
                
                case "Retorno de C√≥digo": {
                    
                    if(jtCatalogo.getSelectedColumn() ==  0) {
                    
                            JOptionPane.showMessageDialog(null,"Valor na coluna selecionada: " + dtm.getValueAt(jtCatalogo.getSelectedRow() ,0).toString() + "." , "System Message", JOptionPane.INFORMATION_MESSAGE);
                
                            jtxtReturn.setText(dtm.getValueAt(jtCatalogo.getSelectedRow() ,0).toString());
                            
                    
                    }
                
                    else
                    
                        JOptionPane.showMessageDialog(null," N√£o selecionou a coluna do c√≥digo.", "System Message", JOptionPane.INFORMATION_MESSAGE);
                
                    break;
                    
                }
                
                default:{
                    
                    JOptionPane.showMessageDialog(null,"Valor na coluna selecionada: " + dtm.getValueAt(jtCatalogo.getSelectedRow() ,jtCatalogo.getSelectedColumn()).toString() + "." , "System Message", JOptionPane.INFORMATION_MESSAGE);
            
                    JOptionPane.showMessageDialog(null,"Nome da coluna selecionada: " + dtm.getColumnName(jtCatalogo.getSelectedColumn()) + "." , "System Message", JOptionPane.INFORMATION_MESSAGE);
                    
                    break;
                    
                    
                }
                
            }
        
            if( (temp.compareTo("Altera√ß√£o de dados") == 0) || (temp.compareTo("Retorno de C√≥digo") == 0) 
                    || (temp.compareTo("Cadastro de √çtem") == 0) )
        
                dispose();
            
    }//GEN-LAST:event_jtCatalogoMousePressed

