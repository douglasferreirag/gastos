    private static void limpar(){ // Limpa a tabela.
        
        dtm =  (javax.swing.table.DefaultTableModel)jtCatalogo.getModel();
         
        for(int i = 0; i < dtm.getRowCount(); i++){
             
                for(int j = 0; j< dtm.getColumnCount(); j++)
                   
                    dtm.setValueAt("", i, j);
             
        }
         
         
        
    }
    
   
    
    private static void preenche (String driver, String url, String user, String password, String query){
     // Preenche a tabela.
     
        
        Connection conn = null;
    
        Statement st = null;
          
         
          
        jtCatalogo.setModel(new javax.swing.table.DefaultTableModel(
                         new Object [][] { }, new String [] {
        
                         "C√≥digo","Descri√ß√£o", "Valor unit√°rio em reais", "C√≥digo do fornecedor do produto/servi√ßo", "Data em que viu o pre√ßo" //Aqui eu nomeio as colunas da tabela de sa√≠da.   
                         }
        ));
          
         
        try
               
        {
                
            
                Class.forName(driver);
                
                conn = DriverManager.getConnection(url, user, password);
       
                // Our SQL SELECT query. 
                
                // If you only need a few columns, specify them by name instead of using "*".
                                                                                                                                            
               // Create the java statement.
                
               st = conn.createStatement();
       
                // Execute the query, and get a java resultset.
               
                ResultSet rs = st.executeQuery(query);
                
               // Iterate through the java resultset.
                
                
               while (rs.next())
                   
               {               
                   
                   
                    dtm = (javax.swing.table.DefaultTableModel)jtCatalogo.getModel();

                    dtm.addRow(new Object[]{rs.getDouble("codigo"),rs.getString("descricao"),rs.getDouble("valorUnitario"),rs.getString("codigoFornecedor"),Funcoes.convertData(rs.getDate("data"), "dd-MM-yyyy ")
                               });
                   
                    
                     
                }
               
             
                st.close();
                
                conn.close();
                
            }
           
            catch (Exception e)
                
            {
                
                
                System.err.println("Got an exception! ");
                 
                System.err.println(e.getMessage());
                
                 
            }

                      
    }
    
