    private void jtableEntradaDadosMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtableEntradaDadosMousePressed
          // Exibe a letra selecionada na tela.
        
        if(num == true) {
            
            if(jtxtVisor.getText().isEmpty() == false) {
                
                    if(jtxtVisor.getText().indexOf('.') != -1) {
                        
                            
                            if (jtableEntradaDados.getSelectedColumn() == 3)
                                    
                                    jtxtVisor.setText(jtxtVisor.getText().concat(dtm.getValueAt(jtableEntradaDados.getSelectedRow(),jtableEntradaDados.getSelectedColumn()).toString()));
        
                        
                    }
                    
                    else{
            
                              if( (jtableEntradaDados.getSelectedColumn() == 3) || (dtm.getValueAt(jtableEntradaDados.getSelectedRow(),jtableEntradaDados.getSelectedColumn()).toString().compareTo(".") == 0) )
                
                                        jtxtVisor.setText(jtxtVisor.getText().concat(dtm.getValueAt(jtableEntradaDados.getSelectedRow(),jtableEntradaDados.getSelectedColumn()).toString()));
        
                    }
                    
                    
            }
            
            else
                        
                    {
                        
                            if (jtableEntradaDados.getSelectedColumn() == 3)
                                    
                                    jtxtVisor.setText(jtxtVisor.getText().concat(dtm.getValueAt(jtableEntradaDados.getSelectedRow(),jtableEntradaDados.getSelectedColumn()).toString()));
        
                        
                    }
         
        }
                
        
        else
            
        {
            
            if( (jtableEntradaDados.getSelectedColumn() != 3) && (dtm.getValueAt(jtableEntradaDados.getSelectedRow(),jtableEntradaDados.getSelectedColumn()).toString().compareTo(".") != 0) )
                
                jtxtVisor.setText(jtxtVisor.getText().concat(dtm.getValueAt(jtableEntradaDados.getSelectedRow(),jtableEntradaDados.getSelectedColumn()).toString()));
        
            
        }
            
            
    
        
        
        
    }//GEN-LAST:event_jtableEntradaDadosMousePressed

