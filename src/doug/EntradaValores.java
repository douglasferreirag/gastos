    private  void limpar(){ // Limpa a tabela.
        
      
         
        for(int i = 0; i < dtm.getRowCount(); i++){
             
                for(int j = 0; j< dtm.getColumnCount(); j++)
                   
                    dtm.setValueAt("", i, j);
             
        }
         
         
        
    }
    
    private void preencher(char[] let, int column){ // Preenchendo a tabela.
        
        for(int i = 0; i < let.length; i++)
        {
            
            dtm.setValueAt(let[i], i, column);
            
        }
        
    }
    
    
    
