    private void jmEnterMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jmEnterMouseClicked
        // Retorna para onde foi chamado.
        
        if(jtxtVisor.getText().isEmpty() == false) {
            
            jtxtReturn.setText(jtxtVisor.getText());
        
            dispose();
            
        }
        
        else 
            
            JOptionPane.showMessageDialog(null, " Não inseriu nada na caixa de texto. ", "System Message", JOptionPane.INFORMATION_MESSAGE);
        
       
        
    }//GEN-LAST:event_jmEnterMouseClicked

