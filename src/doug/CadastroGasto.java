    private void jmCadastroMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jmCadastroMouseClicked
        // Trata do cadastro.
        
       
            jmCadastro.setVisible(false);
        
            String[][] parametro = new String[6][2];
            
            parametro[0][0] = "Date";
            
            parametro[0][1] = jtxtData.getText();
            
            parametro[1][0] = "Double";
            
            parametro[1][1] = jtxtCodigoPessoa.getText();
            
            parametro[2][0] = parametro[1][0];
            
            parametro[2][1] = jtxtCodigoGasto.getText();
            
            parametro[3][0] = parametro[1][0];
            
            parametro[3][1] = jtxtValor.getText();
            
            parametro[4][0] = "String";
            
            parametro[4][1] = jtxtDescricao.getText();
            
            parametro[5][0] = parametro[1][0];
            
            parametro[5][1] = jtxtDesconto.getText();
           
            new Funcoes().executa("INSERT INTO gasto(data,codigoPessoa,codigo,valor, descricao,desconto) VALUES(?,?,?,?,?,?)", parametro, "Inserção na tabela de gastos realizada com sucesso.");
        
        
    }//GEN-LAST:event_jmCadastroMouseClicked

