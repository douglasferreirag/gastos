    private void jtxtValorMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtxtValorMouseClicked
        // Vai para o frame de entrada .
        
       
        
        double desconto = 0.0;
        
        if(jtxtDesconto.getText().isEmpty() == false)
            
            desconto = Double.parseDouble(jtxtDesconto.getText());
        
        double soma = 0.0;
        
        String[][] pesquisa = new String[1][2];
        
        pesquisa[0][0] = "Double";
        
        pesquisa[0][1] = jtxtCodigoGasto.getText();
       
        
        if  (jtxtValor.getText().isEmpty() == true) {
            
             if (new Funcoes().retornar("select sum(total) from item where codigoGasto = ?", "sum(total)", pesquisa) == null) 
            
                        EntradaValores.main(argumento, jtxtValor, true, jtxtUsuario.getText(), "Insira o valor do gasto.");
        
            else {
            
                    soma = new Funcoes().round((Double) new Funcoes().retornar("select sum(total) from item where codigoGasto = ?", "sum(total)", pesquisa));
                    
                    jtxtValor.setText(Double.toString(soma -  desconto));
                    
            }
        
                   
            
        }
        
    }//GEN-LAST:event_jtxtValorMouseClicked

