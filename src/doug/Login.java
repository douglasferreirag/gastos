    private void jmAcessarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jmAcessarMouseClicked
        // Realiza o acesso ao menu principal da aplicação.
        
        String parametro[][] = new String[2][2];
        
        parametro[0][0] = "String";
        
        parametro[0][1] = jtxtUser.getText();
        
        parametro[1][0] = parametro[0][0];
        
        parametro[1][1] = jpfSenha.getText();
        
       
        if(func.retornar("SELECT codigoPessoa  "
                                + " FROM login"
                                + " WHERE ( ( usuario =  ? ) and ( senha =  ? ) )", "codigoPessoa", parametro) != null)  { 
                        
                                        JOptionPane.showMessageDialog(null, " Acessando o menu principal .... ", "System Message", JOptionPane.INFORMATION_MESSAGE);
                
        
                                        MenuPrincipal.main(argumento, jtxtUser.getText());
            
                                        Login.this.dispose();
                
                }  
                
                else 
            
                    JOptionPane.showMessageDialog(null, "Falha na conexão. Digite seus dados novamente.  ", "System Message", JOptionPane.INFORMATION_MESSAGE);

                            
                    
               
                
                
                
        
            
            
        
        
            
    }//GEN-LAST:event_jmAcessarMouseClicked

