    private void jtxtUserKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jtxtUserKeyPressed
        // Trata da entrada do nome de usuário.
       
        if  (evt.getKeyCode() == 10) 
        
        {
            
                jtxtUser.setEditable(false);
                
                jpfSenha.setEditable(true);
            
        }
        
    }//GEN-LAST:event_jtxtUserKeyPressed

    private void jpfSenhaKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jpfSenhaKeyPressed
         // Trata da entrada da senha.
        
        if (evt.getKeyCode() == 10)
        
        {
            
                
                jpfSenha.setEditable(false);
                
                jmAcessar.setVisible(true);
            
        }
        
        
    }//GEN-LAST:event_jpfSenhaKeyPressed

