    private void jtLoginsMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtLoginsMousePressed
         // Retornar um dado valor.
        
        dtm =  (javax.swing.table.DefaultTableModel)jtLogins.getModel();
        
        switch(temp){
                
                case "Alteração de dados":{
                    
                     if((jtLogins.getSelectedColumn() != 0) && (dtm.getValueAt(jtLogins.getSelectedRow() , 3).toString()).compareTo("root") != 0) {
                    
                            JOptionPane.showMessageDialog(null,"Valor na coluna selecionada: " + dtm.getValueAt(jtLogins.getSelectedRow() ,jtLogins.getSelectedColumn()).toString()+ "." , "System Message", JOptionPane.INFORMATION_MESSAGE);
                
                            jtxtReturn.setText(dtm.getValueAt(jtLogins.getSelectedRow() ,jtLogins.getSelectedColumn()).toString());
                
                            String nomeColuna = dtm.getColumnName(jtLogins.getSelectedColumn());
                            
                            JOptionPane.showMessageDialog(null,"Nome da coluna selecionada: " + nomeColuna + "." , "System Message", JOptionPane.INFORMATION_MESSAGE);
                
                            jtxtColumn.setText(nomeColuna);
                            
                            
                    }  
                     
                    break;
                    
                }
                
                case "Retorno de código": {
                    
                    if(jtLogins.getSelectedColumn() ==  0) {
                    
                            JOptionPane.showMessageDialog(null,"Valor na coluna selecionada: " + dtm.getValueAt(jtLogins.getSelectedRow() ,0).toString() + "." , "System Message", JOptionPane.INFORMATION_MESSAGE);
                
                            jtxtReturn.setText(dtm.getValueAt(jtLogins.getSelectedRow() ,0).toString());
                    
                    }
                
                    else
                    
                        JOptionPane.showMessageDialog(null," Não selecionou a coluna do código da pessoa.", "System Message", JOptionPane.INFORMATION_MESSAGE);
                
                    break;
                    
                }
                
                default:{
                    
                    JOptionPane.showMessageDialog(null,"Valor na coluna selecionada: " + dtm.getValueAt(jtLogins.getSelectedRow() ,jtLogins.getSelectedColumn()).toString() + "." , "System Message", JOptionPane.INFORMATION_MESSAGE);
            
                    JOptionPane.showMessageDialog(null,"Nome da coluna selecionada: " + dtm.getColumnName(jtLogins.getSelectedColumn()) + "." , "System Message", JOptionPane.INFORMATION_MESSAGE);
                    
                    break;
                    
                    
                }
                
            }
        
            if( (temp.compareTo("Alteração de dados") == 0) || (temp.compareTo("Retorno de código") == 0) )
        
                dispose();
            
    }//GEN-LAST:event_jtLoginsMousePressed

