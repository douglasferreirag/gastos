   private static void preenche (String driver, String url, String user, String password, String query){
     // Preenche a tabela.
     
        
        Connection conn = null;
    
        Statement st = null;
          
         
          
        jtLogins.setModel(new javax.swing.table.DefaultTableModel(
                         new Object [][] { }, new String [] {
        
                         "Código da pessoa","Usuário", "Senha", "Tipo" //Aqui eu nomeio as colunas da tabela de saída.    
                         }
        ));
          
         
        try
               
        {
                
                Class.forName(driver);
                
                conn = DriverManager.getConnection(url, user, password);
       
                // Our SQL SELECT query. 
                
                // If you only need a few columns, specify them by name instead of using "*".
                                                                                                                                            
               // Create the java statement.
                
               st = conn.createStatement();
       
                // Execute the query, and get a java resultset.
               
                ResultSet rs = st.executeQuery(query);
                
               // Iterate through the java resultset.
                
               while (rs.next())
                   
               {        
                   
                    
                   
                     dtm = (javax.swing.table.DefaultTableModel)jtLogins.getModel();

                    dtm.addRow(new Object[]{rs.getDouble("codigoPessoa"),rs.getString("usuario"),rs.getString("senha"), rs.getString("poderes")
                               });
                   
                     
                }
             
                st.close();
                
                conn.close();
            }
           
            catch (Exception e)
                
            {
                 System.err.println("Got an exception! ");
                 
                 System.err.println(e.getMessage());
                 
            }

                      
    }
    
    
