    private void jmCadastroMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jmCadastroMouseClicked
        // Trata do cadastro de login.
      
                jmCadastro.setVisible(false);
        
                String[][] parametro  = new String[4][2];
                
                parametro[0][0] = "Double";
                
                parametro[0][1] = jtxtCodigoPessoa.getText();
                
                parametro[1][0] = "String";
                
                parametro[1][1] = jtxtLogin.getText();
                
                parametro[2][0] = parametro[1][0];
                
                parametro[2][1] = jpfSenha.getText();
        
                parametro[3][0] = parametro[1][0];
                
                parametro[3][1] = jtxtTipo.getText();
                
               
                func.executa("INSERT INTO login(codigoPessoa, usuario, senha, poderes) VALUES(?,?,?,?)", parametro, "Cadastro na tabela de login com sucesso. ");
                
                
                
        
              
    }//GEN-LAST:event_jmCadastroMouseClicked

    private void jtxtLoginMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtxtLoginMouseClicked
        // Vai para o frame de entrada.
        
        if(jtxtLogin.getText().isEmpty() == true)
            
            EntradaValores.main(argumento, jtxtLogin, false, jtxtUsuario.getText(), "Insira o nome de usuário.");
        
    }//GEN-LAST:event_jtxtLoginMouseClicked

