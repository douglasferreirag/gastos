    private void formWindowActivated(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowActivated
        
        // Verifica se posso liberar o botão de cadastro.
  
        
        
         if((jtxtCodigoPessoa.getText().isEmpty() == false) && (jtxtLogin.getText().isEmpty() == false) && 
            (jpfSenha.getText().isEmpty() == false)  && (trava == false) && (jtxtTipo.getText().isEmpty() == false)
                 
                ) {
             
                jmCadastro.setVisible(true);
                
                trava = true;
             
         }
         
       
         
    }//GEN-LAST:event_formWindowActivated

    private void jtxtCodigoPessoaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtxtCodigoPessoaMouseClicked
        //Vai para o frame de listagem de pessoas.
        
        if(jtxtCodigoPessoa.getText().isEmpty() == true)
            
            ListagemPessoa.main(argumento, jtxtCodigoPessoa, null,"Retorno de código", false, jtxtUsuario.getText(), "Selecione o código da pessoa que ficará associado ao login que será criado.");
        
        
    }//GEN-LAST:event_jtxtCodigoPessoaMouseClicked

    private void jtxtTipoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtxtTipoMouseClicked
        //Vai para o frame de seleção de tipo de usuário.
        
        if(jtxtTipo.getText().isEmpty() == true)
            
            PoderesUsuario.main(argumento, jtxtUsuario.getText(), jtxtTipo, "Selecione o tipo de acesso que será feito por este login.", "cadastro");
        
    }//GEN-LAST:event_jtxtTipoMouseClicked

