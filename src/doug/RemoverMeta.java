    private void jmRemoverMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jmRemoverMouseClicked
        // Remoção do registro. Atualização da tabela.
        
        jmRemover.setVisible(false);
        
        String[][] parametro = new String[1][2];
        
        JOptionPane.showMessageDialog(null, " Deletando...", "System Message", JOptionPane.INFORMATION_MESSAGE);
      
        parametro[0][0] = "Double";
        
        parametro[0][1] = jtxtCodigo.getText();    

        new Funcoes().executa("delete from meta where codigo = ?",
                parametro, "Remoção na tabela de metas realizada com sucesso.");
        
        if ( (new Funcoes().retornar("SELECT Max(codigo) "
            + "FROM meta ","Max(codigo)",null) != null)&& (Double.parseDouble(jtxtCodigo.getText()) < (Double)new Funcoes().retornar("SELECT Max(codigo) "
            + "FROM meta ","Max(codigo)",null)) )

            new Funcoes().executa( "update meta set codigo = codigo - 1 where codigo > ? ", parametro,"Atualização de códigos na tabela de metas realizada com sucesso.");

       
        
    }//GEN-LAST:event_jmRemoverMouseClicked

    private void jtxtCodigoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtxtCodigoMouseClicked
        // Seleciona o código da meta.
        
        if(jtxtCodigo.getText().isEmpty() == true)
            
            ListagemMeta.main(argumento, jtxtCodigo, null,null,"Retorno de código", false, jtxtUsuario.getText(), "Selecione o código do registro a ser removido.");
        
    }//GEN-LAST:event_jtxtCodigoMouseClicked

