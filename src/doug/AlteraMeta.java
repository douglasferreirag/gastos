    private void formWindowActivated(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowActivated
        // Pega o dado que será alterado.
        
       if(jtxtDesc.getText().compareTo("Código") == 0) 
                
                alterado = "codigo";
                
              
            
        if(jtxtDesc.getText().compareTo("Mês") == 0) 
                
                alterado = "mes";
                
            
        
        if(jtxtDesc.getText().compareTo("Ano") == 0) 
                
                alterado = "ano";
        
        
        if(jtxtDesc.getText().compareTo("Valor em reais") == 0) 
                
                alterado = "valor";
        
        // Vai decidir se pode ou não liberar o botão de alterar.
        
        if (
            
                
                
                        (jtxtDesc.getText().isEmpty() == false) &&
                
                        (jtxtConteudo.getText().isEmpty() == false) &&
                
                        (jtxtNovoConteudo.getText().isEmpty() == false) && (trava == false)
                
              
                
                
            ) {
            
                trava = true;
                
                jmAltera.setVisible(true);
                
        }
            
      
    }//GEN-LAST:event_formWindowActivated

    private void jtxtNovoConteudoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtxtNovoConteudoMouseClicked
        //Dependendo do que for alterar irá mandar para uma tela de entrada de dados diferente.
        
        if ( (jtxtDesc.getText().isEmpty() == false) && (jtxtNovoConteudo.getText().isEmpty() == true)) {
            
            if ( (jtxtDesc.getText().compareTo("Código") == 0 ) || (jtxtDesc.getText().compareTo("Valor em reais") == 0 ) )
                
                EntradaValores.main(argumento, jtxtNovoConteudo, true, jtxtUsuario.getText(), "Insira o " + jtxtDesc.getText() + ".");
            
            else
                
            {
                
                if(jtxtDesc.getText().compareTo("Mês") == 0 )
                    
                     Data.main(argumento, jtxtNovoConteudo,1, "Insira o mês referente à meta", jtxtUsuario.getText() + ".");
        
                
                else
                    
                   Data.main(argumento, jtxtNovoConteudo,2, "Insira o ano correspondente ao mês anotado para a meta.", jtxtUsuario.getText());
          
                
            }
            
        }
            
            
        
    }//GEN-LAST:event_jtxtNovoConteudoMouseClicked

    private void jtxtDescMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtxtDescMouseClicked
        // Seleciona o campo a ser alterado.
        
        if(jtxtDesc.getText().length() == 0)
            
            ListagemMeta.main(argumento, jtxtConteudo, jtxtDesc,jtxtCod,"Alteração de dados", false,jtxtUsuario.getText(), "Selecione o registro a ser alterado.");
            
        
    }//GEN-LAST:event_jtxtDescMouseClicked

    private void jmAlteraMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jmAlteraMouseClicked
        // Trata da alteração.
        
        jmAltera.setVisible(false);
            
        String d[][] = new String[2][2];

        d[0][0] = "Double";

        d[0][1] = jtxtNovoConteudo.getText();
        
        d[1][0] = d[0][0];
        
        d[1][1] = jtxtCod.getText();
                
        new Funcoes().executa("update meta set " + alterado + " = ? where codigo =  ? " , d, "Alteração na tabela de metas realizada com sucesso.");
       
            
       
        
    }//GEN-LAST:event_jmAlteraMouseClicked
   
