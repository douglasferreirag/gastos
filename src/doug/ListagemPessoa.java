    private void jtPessoasMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtPessoasMousePressed
         // Retornar um dado valor.
        
        dtm =  (javax.swing.table.DefaultTableModel)jtPessoas.getModel();
        
        switch(temp){
                
                case "Alteração de dados":{
                    
                    JOptionPane.showMessageDialog(null,"Valor na coluna selecionada: " + dtm.getValueAt(jtPessoas.getSelectedRow() ,jtPessoas.getSelectedColumn()).toString() + "." , "System Message", JOptionPane.INFORMATION_MESSAGE);
                
                    jtxtReturn.setText(dtm.getValueAt(jtPessoas.getSelectedRow() ,jtPessoas.getSelectedColumn()).toString());
                
                    String nomeColuna = dtm.getColumnName(jtPessoas.getSelectedColumn());
                            
                    JOptionPane.showMessageDialog(null,"Nome da coluna selecionada: " + nomeColuna + "." , "System Message", JOptionPane.INFORMATION_MESSAGE);
                
                    jtxtColumn.setText(nomeColuna);
                    
                    break;
                    
                }
                
                case "Retorno de código": {
                    
                    if(jtPessoas.getSelectedColumn() ==  0) {
                    
                            JOptionPane.showMessageDialog(null,"Valor na coluna selecionada: " + dtm.getValueAt(jtPessoas.getSelectedRow() ,0).toString() + "." , "System Message", JOptionPane.INFORMATION_MESSAGE);
                
                            jtxtReturn.setText(dtm.getValueAt(jtPessoas.getSelectedRow() ,0).toString());
                    
                    }
                
                    else
                    
                        JOptionPane.showMessageDialog(null," Não selecionou a coluna do código.", "System Message", JOptionPane.INFORMATION_MESSAGE);
                
                    break;
                    
                }
                
                default:{
                    
                    JOptionPane.showMessageDialog(null,"Valor na coluna selecionada: " + dtm.getValueAt(jtPessoas.getSelectedRow() ,jtPessoas.getSelectedColumn()).toString() + "." , "System Message", JOptionPane.INFORMATION_MESSAGE);
            
                    JOptionPane.showMessageDialog(null,"Nome da coluna selecionada: " + dtm.getColumnName(jtPessoas.getSelectedColumn()) + "." , "System Message", JOptionPane.INFORMATION_MESSAGE);
        
                    break;
                    
                }
                
            }
            
            if( (temp.compareTo("Alteração de dados") == 0) || (temp.compareTo("Retorno de código") == 0) )
        
                dispose();
        
    }//GEN-LAST:event_jtPessoasMousePressed

