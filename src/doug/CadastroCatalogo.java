    private void jmCadastroMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jmCadastroMouseClicked
        // Trata do cadastro.
        
                jmCadastro.setVisible(false);
        
                String[][] parametro = new String[5][2];
            
                parametro[0][0] = "Double";
            
                parametro[0][1] = jtxtCod.getText();
            
                parametro[1][0] = "String";
            
                parametro[1][1] = jtxtDesc.getText();
            
                parametro[2][0] = parametro[0][0];
            
                parametro[2][1] = jtxtVal.getText();
        
                parametro[3][0] = "Double";
            
                parametro[3][1] = jtxtCodigoFornecedor.getText();
                
                parametro[4][0] = "Date";
            
                parametro[4][1] = jtxtData.getText();
         
            
                new Funcoes().executa("INSERT INTO catalogo(codigo,descricao,valorUnitario, codigoFornecedor,data) VALUES(?,?,?,?,?)", parametro, "Inserção na tabela de produtos/serviços catalogados realizada com sucesso.");
            
               
            
        
        
    }//GEN-LAST:event_jmCadastroMouseClicked

    private void jmLimparMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jmLimparMouseClicked
        // Limpa o formulário.
        
        new Funcoes().setaCodigo(jtxtCod,"SELECT Max(codigo) "
                          + "FROM catalogo ","Max(codigo)");
        
        jtxtDesc.setText("");
        
        jtxtVal.setText("");
        
        jtxtCodigoFornecedor.setText("");

        trava = false;
        
        jmCadastro.setVisible(false);
        
        
    }//GEN-LAST:event_jmLimparMouseClicked

