    private void jtxtDescMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtxtDescMouseClicked
        //Seleciona o campo a ser alterado.
        
        if(jtxtDesc.getText().isEmpty() == true)
        
                ListagemLogin.main(argumento, jtxtConteudo, jtxtDesc,"Alteração de dados", false, "Selecione o registro a ser modificado.", jtxtUsuario.getText());
        
        
        
    }//GEN-LAST:event_jtxtDescMouseClicked

    private void jtxtNovoConteudoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtxtNovoConteudoMouseClicked
        // Se o campo descrição estiver preenchido, chama o frame de entrada.
        
        if( (jtxtDesc.getText().isEmpty() == false) &&  (jtxtNovoConteudo.getText().isEmpty() == true)) {
            
            
            if (jtxtDesc.getText().compareTo("Tipo") == 0) 
                
                PoderesUsuario.main(argumento, jtxtUsuario.getText(), jtxtNovoConteudo, "Selecione o novo tipo do usuário.", "cadastro");
            
            else
                
                EntradaValores.main(argumento, jtxtNovoConteudo, false, jtxtUsuario.getText(), "Insira o(a) " + jtxtDesc.getText() + ".");
        
            
        }
            
            
    }//GEN-LAST:event_jtxtNovoConteudoMouseClicked

    private void jmAlterarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jmAlterarMouseClicked
         // Tratando de alterar dados da tabela login.
       
        jmAlterar.setVisible(false);
        
        String d[][] = new String[2][2];
        
        d[0][0] = "String";
        
        d[1][0] = d[0][0];
       
        d[1][1] = jtxtConteudo.getText().trim();
       
        d[0][1] = jtxtNovoConteudo.getText().trim(); 
 
        new Funcoes().executa("update login set " + alterado + " = ?  where " +  alterado + " =  ? " , d, "Alteração na tabela de login realizada com sucesso.");
        
        
      
        
    }//GEN-LAST:event_jmAlterarMouseClicked

    private void formWindowActivated(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowActivated
        // Pega o dado que será alterado.
        
        // Vai decidir se pode ou não liberar o botão de alterar.
        
        if(jtxtDesc.getText().compareTo("Usuário") == 0)
            
                alterado = "usuario";
         
        if(jtxtDesc.getText().compareTo("Senha") == 0)
            
                alterado = "senha";
        
        if(jtxtDesc.getText().compareTo("Tipo") == 0)
            
                alterado = "poderes";
      
      
        
        if (
            
                
                
                        (jtxtDesc.getText().isEmpty() == false) &&
                
                        (jtxtConteudo.getText().isEmpty() == false) &&
                
                        (jtxtNovoConteudo.getText().isEmpty() == false) && 
                
                        (trava == false)
                
                
            ) {
            
                trava = true;
                
                jmAlterar.setVisible(true);
                
                
                
        }
        
        
    }//GEN-LAST:event_formWindowActivated
    
    
