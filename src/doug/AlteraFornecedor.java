    private void jmAlterarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jmAlterarMouseClicked
        // Trata da alteração.
        
        jmAlterar.setVisible(false);
        
        String alterado = "";
        
        String d[][] = new String[2][2];
        
        d[0][1] = jtxtNovoConteudo.getText().trim();
   
        d[1][1] = jtxtConteudo.getText().trim();
        
        if(jtxtDesc.getText().compareTo("Código") == 0) {
                
                alterado = "codigo";
                
                d[0][0] = "Double";
                
                d[1][0] = d[0][0];
                
        }
              
            
        if(jtxtDesc.getText().compareTo("Descrição") == 0) {
                
                alterado = "descricao";
                
                d[0][0] = "String";
                
                d[1][0] = d[0][0];
                
                
        }
        
          if(jtxtDesc.getText().compareTo("Local") == 0) {
                
                alterado = "local";
                
                d[0][0] = "String";
                
                d[1][0] = d[0][0];
                
                
                
        }
          
          
        String c1[][] = new String[1][2];
                
        c1[0][0] = d[0][0];
                
        c1[0][1] = jtxtConteudo.getText();  
          
        if(new Funcoes().retornar("select descricao from catalogo where codigoFornecedor = ?", "descricao", c1) != null)
 
               
                    new Funcoes().executa("update catalogo set codigofornecedor" + " = ? where " +  alterado + " =  ? " , d, "Alteração na tabela de gastos realizada com sucesso.");

            
        
       
     
        new Funcoes().executa("update fornecedor set " + alterado + " = ? where " +  alterado + " =  ? " , d, "Alteração na tabela de fornecedores realizada com sucesso.");

       
        
    }//GEN-LAST:event_jmAlterarMouseClicked

    private void formWindowActivated(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowActivated
        // Vai decidir se pode ou não liberar o botão de alterar.
        
        if (
            
                
                
                        (jtxtDesc.getText().isEmpty() == false) &&
                
                        (jtxtConteudo.getText().isEmpty() == false) &&
                
                        (jtxtNovoConteudo.getText().isEmpty() == false) && (trava == false)
                
              
                
                
            ) {
            
                trava = true;
        
                jmAlterar.setVisible(true);
                 
        }
        
    }//GEN-LAST:event_formWindowActivated

