    private static void preenche (String driver, String url, String user, String password, String query){
     // Preenche a tabela.
     
        
        Connection conn = null;
    
        Statement st = null;
          
         
          
        jtMetas.setModel(new javax.swing.table.DefaultTableModel( //Aqui é adicionado as colunas junto ao seus nomes.
                         new Object [][] { }, new String [] {
        
                         "Código","Mês", "Ano", "Valor em reais",
                         "Código da pessoa"     
                         }
        ));
          
        
         
        try
               
        {
                
                Class.forName(driver);
                
                conn = DriverManager.getConnection(url, user, password);
       
                // Our SQL SELECT query. 
                
                // If you only need a few columns, specify them by name instead of using "*".
                                                                                                                                            
               // Create the java statement.
                
               st = conn.createStatement();
       
                // execute the query, and get a java resultset
               
                ResultSet rs = st.executeQuery(query);
                
                
               // Iterate through the java resultset.
                
               while (rs.next())
                   
               {                 
                   
                   
                    javax.swing.table.DefaultTableModel dtm = (javax.swing.table.DefaultTableModel)jtMetas.getModel();

                    dtm.addRow(new Object[]{rs.getDouble("codigo"),rs.getDouble("mes"),rs.getDouble("ano"), 
                                rs.getDouble("valor"), rs.getDouble("codigoPessoa")});
                   
                     
                }
             
                st.close();
                
                conn.close();
            }
           
            catch (Exception e)
                
            {
                 System.err.println("Got an exception! ");
                 
                 System.err.println(e.getMessage());
                 
            }

                      
    }
    

