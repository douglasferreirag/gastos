    private void jtMetasMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtMetasMousePressed
         // Retornar um dado valor.
        
        
        dtm =  (javax.swing.table.DefaultTableModel)jtMetas.getModel();
        
        switch(temp){
                
                case "Alteração de dados":{
         
                    if(jtMetas.getSelectedColumn() != 4) {
                    
                            JOptionPane.showMessageDialog(null,"Valor na coluna selecionada: " + dtm.getValueAt(jtMetas.getSelectedRow() ,jtMetas.getSelectedColumn()).toString() + "." , "System Message", JOptionPane.INFORMATION_MESSAGE);
                
                            jtxtReturn.setText(dtm.getValueAt(jtMetas.getSelectedRow() ,jtMetas.getSelectedColumn()).toString());
                
                            String nomeColuna = dtm.getColumnName(jtMetas.getSelectedColumn());
                            
                            JOptionPane.showMessageDialog(null,"Nome da coluna selecionada: " + nomeColuna + "." , "System Message", JOptionPane.INFORMATION_MESSAGE);
                
                            jtxtCodigo.setText(dtm.getValueAt(jtMetas.getSelectedRow() ,0).toString());
                            
                            jtxtColumn.setText(nomeColuna);
                            
                    }        
                   
                    break;
                    
                }
                
                case "Retorno de código": {
                    
                    if(jtMetas.getSelectedColumn() ==  0) {
                    
                            JOptionPane.showMessageDialog(null,"Valor na coluna selecionada: " + dtm.getValueAt(jtMetas.getSelectedRow() ,0).toString() + "." , "System Message", JOptionPane.INFORMATION_MESSAGE);
                
                            jtxtReturn.setText(dtm.getValueAt(jtMetas.getSelectedRow() ,0).toString());
                            
                            
                    }
                
                    else
                    
                        JOptionPane.showMessageDialog(null," Não selecionou a coluna do código.", "System Message", JOptionPane.INFORMATION_MESSAGE);
                
                    break;
                    
                }
                
               
                
                default:{
                    
                    JOptionPane.showMessageDialog(null,"Valor na coluna selecionada: " + dtm.getValueAt(jtMetas.getSelectedRow() ,jtMetas.getSelectedColumn()).toString() + "." , "System Message", JOptionPane.INFORMATION_MESSAGE);
            
                    JOptionPane.showMessageDialog(null,"Nome da coluna selecionada: " + dtm.getColumnName(jtMetas.getSelectedColumn()) + "." , "System Message", JOptionPane.INFORMATION_MESSAGE);
                    
                    break;
                    
                    
                }
                
            }
        
            if( (temp.compareTo("Alteração de dados") == 0) || (temp.compareTo("Retorno de código") == 0) )
        
                dispose();
            
    }//GEN-LAST:event_jtMetasMousePressed

